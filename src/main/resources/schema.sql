CREATE TABLE IF NOT EXISTS FLAT_OWNER (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL PRIMARY KEY,
  FIRST_NAME VARCHAR(255),
  LAST_NAME VARCHAR(255),
  ANOTHER_NAMES VARCHAR(255),
  BIRTH_DATE DATE,
  EMAIL VARCHAR(255),
  PHONE VARCHAR(14),
  NOTICE VARCHAR(1024),
);

CREATE TABLE IF NOT EXISTS FLAT (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL PRIMARY KEY,
  ADDRESS VARCHAR(255),
  NUMBER VARCHAR(255)
);

CREATE TABLE IF NOT EXISTS OWNERSHIP (
  OWNER_ID INTEGER NOT NULL,
  FLAT_ID INTEGER NOT NULL,
  OWNERSHIP_TYPE CHAR(1) NOT NULL CHECK (OWNERSHIP_TYPE IN ('O', 'S'))
  -- O - Owner
  -- S - cooperative Share
);

CREATE TABLE IF NOT EXISTS ADDRESS (
  ADDRESS_ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL PRIMARY KEY,
  MUNICIPALITY VARCHAR(35),
  MUNICIPALITY_PART VARCHAR(35),
  STREET VARCHAR(35),
  HOUSE_NUMBER VARCHAR(35),
);
